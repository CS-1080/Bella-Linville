    <h2 class="border">Decreased Chemical Pollution</h2>
    <p>In order to maintain a bright green, weed free lawn, fertilizers and herbicides are required. The chemical ingredients within these lawn treatments may benefit your plot of land, but not your local ecosystem. When you water your lawn, or when it rains,
        these chemicals run off of your lawn at toxic levels. These chemicals are capable or leeching into groundwater, an important source of fresh drinking water, or running into bodies of water near you. Upon reaching the nearest body of water, the nitrogen and pottasium commonly found in these treatments promotes weed and algae growth.
        These algal blooms lead to an event called eutrification, which can have deadly effects on the wildlife inhabiting that ecosystem. Fish directly affected by this process, as these cycles of algal blooms and die offs depletes the water of oxygen, essentially suffocating them. The rest of that ecosystems food chain is then indirectly affected by the absence of fish, creating a trophic cascade which affects all beings in that body of water. 
        Ditching grass lawns, and therefore the need for treatments, alleviates this additional stress put on your local ecosystem. Trust me, the fish will thank you!</p>
